#include "ft_printf.h"

void	i_print(long int n, int *i)
{
	if (n == 0)
	{
		write(1, "0", 1);
		*i = *i + 1;
		return ;
	}
	if (n == -2147483648)
	{
		write(1, "-2147483648", 11);
		*i = *i + 11;
		return ;
	}
	if (n < 0)
	{
		n = -n;
		write(1, "-", 1);
		*i = *i + 1;
	}
	if (n / 10)
		i_print(n / 10, i);
	ft_putchar_fd(n % 10 + '0', 1);
	*i = *i + 1;
}
